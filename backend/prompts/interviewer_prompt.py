# User Prompt 模板
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

INTERVIEWER_USER_TEMPLATE = """## 用户信息
- 技术栈：{tech_stack}
- 目标公司：{target_company}
- 目标岗位：{target_position}

## 参考资料（题库检索结果）
{reference_materials}

## 用户请求
{user_request}

## 要求
1. 只使用提供的参考资料
2. 回复必须友好、专业
3. 必须给出具体建议
4. 必须基于事实，不编造
"""


def format_interviewer_user_prompt(
    user_info: dict,
    reference_materials: str,
    user_request: str
) -> str:
    """
    格式化User Prompt
    
    Args:
        user_info: 用户信息字典
        reference_materials: 参考资料（RAG检索结果）
        user_request: 用户请求
    
    Returns:
        格式化后的User Prompt
    """
    return INTERVIEWER_USER_TEMPLATE.format(
        tech_stack=user_info.get('tech_stack', '未设置'),
        target_company=user_info.get('target_company', '未设置'),
        target_position=user_info.get('target_position', '未设置'),
        reference_materials=reference_materials,
        user_request=user_request
    )
