def _count_numbered_questions(content: str) -> int:
    """统计原文中编号题目的数量（如 1. 2. 3. ... 43.），支持同行多编号如「1. xxx 2. yyy」"""
    import re
    # 匹配 数字. 或 数字、 格式（含行内如「1. 题目 2. 题目」）
    matches = re.findall(r"(?<!\d)(\d+)[\.、]\s*", content)
    if not matches:
        return 0
    nums = [int(m) for m in matches]
    return max(nums) if nums else 0


def _has_tech_topics(content: str) -> bool:
    """正文是否包含技术话题描述（八股、数据库、多线程等）"""
    _keywords = ("八股", "数据库", "kafka", "多线程", "spring", "高并发", "redis", "mysql", "es ")
    c = (content or "").lower()
    return any(k in c for k in _keywords)


def format_miner_user_prompt(content: str, has_image: bool, company: str = "", position: str = "") -> str:
    """格式化用户提示词"""
    meta_parts = []
    if company:
        meta_parts.append(f"\n- 公司：{company}")
    if position:
        meta_parts.append(f"\n- 岗位：{position}")
    # 正文有技术话题时，强制提示直接输出 JSON，禁止 OCR
    if has_image and _has_tech_topics(content):
        meta_parts.append("\n- **【正文已有技术话题】直接输出 JSON 数组，禁止调用 ocr_images，禁止输出任何解释性文字**")
    # 若检测到编号列表，提示题目总数，减少漏提
    n = _count_numbered_questions(content)
    if n >= 10:
        meta_parts.append(
            f"\n- **原文约 {n} 道题（编号 1-{n}），必须逐条提取 {n} 道，禁止遗漏或编造**"
            f"\n- **提取完成后必须先调用 verify_extraction_count(extracted_count=你提取的数量, expected_count={n}) 校验，通过后直接输出 JSON 数组**"
        )
    meta_hint = "".join(meta_parts)
    ocr_hint = (
        "\n- **重要**：正文仅有话题标签（#面经 #面试 等）无实际题目时，**必须**先调用 ocr_images；"
        "正文有编号/问句/技术话题（八股、数据库、Kafka、多线程、spring 等）时，**禁止**调用 ocr_images，直接提取（图片可能是招聘流程图等无关图）。"
        if has_image else ""
    )
    return MINER_USER_PROMPT_TEMPLATE.format(
        content=content,
        has_image="是" if has_image else "否",
        meta_hint=meta_hint,
        ocr_hint=ocr_hint,
    )