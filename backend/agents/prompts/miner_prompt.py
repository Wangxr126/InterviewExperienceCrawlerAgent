def _count_numbered_questions(content: str) -> int:
    """统计原文中编号题目的数量（如 1. 2. 3. ... 43.）"""
    import re
    # 匹配 数字. 或 数字、 格式
    matches = re.findall(r"^\s*(\d+)[\.、]\s*", content, re.MULTILINE)
    if not matches:
        return 0
    nums = [int(m) for m in matches]
    return max(nums) if nums else 0


